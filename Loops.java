public class Loop
{
    public static void main(String a[])
    {
        int i = 1;

        // A while loop. This is used to interate when a certain condition is true
        while(i <= 4)
        {
            System.out.println("Hi " + i);
            i++; //increment the value of i
        }

        System.out.println("Bye " + i );
    }
}